// JSON = JavaScript Object Notation;
// Utilizado para comunicação entre serviços, ex.: Back-end <-> Front-end.
// Basicamente um tipo de dado padronizado, que lembra os objetos do JavaScript;

let pessoa = {
    "nome": "Mateus",
    "idade": 26
}

console.log(pessoa.nome);
console.log(pessoa.idade);

// O JSON é muito Rigoroso na estrutura, ou seja, você deve seguir o padrão imposto pelo formato. 
// Apenas aspas duplas;
// Não aceita comentários.