// indexOf - encontra o indice onde o elemento se encontra pela primeira vez; 
// lastIndexOf - encontra o indice onde o elemento se encontra pela ultima vez; 

let numeros = [1, 2, 1, 3, 4, 5, 4, 1]; 

console.log(numeros.indexOf(1));
console.log(numeros.lastIndexOf(1));

console.log(numeros.indexOf(4));
console.log(numeros.lastIndexOf(4));
