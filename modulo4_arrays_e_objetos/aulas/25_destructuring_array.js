//Podemos definir variaveis com os valores de um array utilizando o destructuring. 
console.clear();
let numeros = [2, 4, 5, 8];

//Atribui os valores nas variaveis correspondentes aos indices da array.
let [num1, num2, num3, num4] = numeros;

console.log(num1);
console.log(num2);
console.log(num3);
console.log(num4);